-- Parent-teacher controlled messaging.
-- Text-only first phase.

create table if not exists public.parent_teacher_messages (
  id bigint generated by default as identity primary key,
  student_id bigint not null references public.students(id) on delete cascade,
  parent_id uuid references public.profiles(id) on delete set null,
  teacher_id uuid references public.profiles(id) on delete set null,
  sender_id uuid not null references public.profiles(id) on delete cascade,
  body text not null check (char_length(trim(body)) > 0 and char_length(body) <= 2000),
  parent_read_at timestamptz,
  teacher_read_at timestamptz,
  created_at timestamptz not null default now()
);

create index if not exists parent_teacher_messages_student_created_idx
on public.parent_teacher_messages(student_id, created_at);

create index if not exists parent_teacher_messages_sender_idx
on public.parent_teacher_messages(sender_id, created_at desc);

alter table public.parent_teacher_messages enable row level security;

drop policy if exists parent_teacher_messages_select on public.parent_teacher_messages;
create policy parent_teacher_messages_select
on public.parent_teacher_messages
for select
to authenticated
using (
  exists (
    select 1 from public.profiles p
    where p.id = auth.uid() and p.role = 'admin'
  )
  or exists (
    select 1
    from public.parent_student ps
    where ps.parent_id = auth.uid()
      and ps.student_id = parent_teacher_messages.student_id
  )
  or exists (
    select 1
    from public.students s
    join public.teacher_class tc on tc.class_id = s.class_id
    where s.id = parent_teacher_messages.student_id
      and tc.teacher_id = auth.uid()
  )
);

drop policy if exists parent_teacher_messages_insert on public.parent_teacher_messages;
create policy parent_teacher_messages_insert
on public.parent_teacher_messages
