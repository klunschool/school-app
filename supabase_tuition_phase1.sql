drop policy if exists payments_read on public.payments;
create policy payments_read on public.payments
for select to authenticated using (
  public.is_finance_staff()
  or parent_id = auth.uid()
  or exists (
    select 1 from public.student_invoices i
    where i.id = invoice_id and public.can_access_student(i.student_id)
  )
);
drop policy if exists payments_parent_insert on public.payments;
create policy payments_parent_insert on public.payments
for insert to authenticated with check (
  parent_id = auth.uid()
  and status = 'pending'
  and exists (
    select 1
    from public.student_invoices i
    join public.parent_student ps on ps.student_id = i.student_id
    where i.id = invoice_id and ps.parent_id = auth.uid()
  )
);
drop policy if exists payments_staff_update on public.payments;
create policy payments_staff_update on public.payments
for update to authenticated using (public.is_finance_staff())
with check (public.is_finance_staff());

drop policy if exists receipts_read on public.receipts;
create policy receipts_read on public.receipts
for select to authenticated using (
  public.is_finance_staff()
  or exists (
    select 1 from public.payments p
    join public.student_invoices i on i.id = p.invoice_id
    where p.id = payment_id and public.can_access_student(i.student_id)
  )
);

